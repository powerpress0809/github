package Omok;

public class Player {
	String name;
    String stone;
    
    Player(String name, String stone) {
        this.name = name;
        this.stone = stone;
    }

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getStone() {
		return stone;
	}

	public void setStone(String stone) {
		this.stone = stone;
	}
    
    
    
    
    
    
}